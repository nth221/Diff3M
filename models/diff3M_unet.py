class FeedForward(nn.Module):
    def __init__(self, dim, hidden_dim, dropout = 0.):
        super().__init__()
        self.net = nn.Sequential(
            nn.LayerNorm(dim),
            nn.Linear(dim, hidden_dim),
            nn.GELU(),
            nn.Dropout(dropout),
            nn.Linear(hidden_dim, dim),
            nn.Dropout(dropout)
        )

    def forward(self, x):
        return self.net(x)

class Attention(nn.Module):
    def __init__(self, dim, heads = 8, dim_head = 64, dropout = 0.):
        super().__init__()
        inner_dim = dim_head *  heads
        project_out = not (heads == 1 and dim_head == dim)

        self.heads = heads
        self.scale = dim_head ** -0.5

        self.norm = nn.LayerNorm(dim)

        self.attend = nn.Softmax(dim = -1)
        self.dropout = nn.Dropout(dropout)

        self.to_qkv = nn.Linear(dim, inner_dim * 3, bias = False)

        self.to_out = nn.Sequential(
            nn.Linear(inner_dim, dim),
            nn.Dropout(dropout)
        ) if project_out else nn.Identity()

    def forward(self, x):
        x = self.norm(x)

        qkv = self.to_qkv(x).chunk(3, dim = -1)
        q, k, v = map(lambda t: rearrange(t, 'b n (h d) -> b h n d', h = self.heads), qkv)

        dots = th.matmul(q, k.transpose(-1, -2)) * self.scale

        attn = self.attend(dots)
        attn = self.dropout(attn)

        out = th.matmul(attn, v)
        out = rearrange(out, 'b h n d -> b n (h d)')
        return self.to_out(out)

class Transformer(nn.Module):
    def __init__(self, dim, depth, heads, dim_head, mlp_dim, dropout = 0.):
        super().__init__()
        self.norm = nn.LayerNorm(dim)
        self.layers = nn.ModuleList([])
        for _ in range(depth):
            self.layers.append(nn.ModuleList([
                Attention(dim, heads = heads, dim_head = dim_head, dropout = dropout),
                FeedForward(dim, mlp_dim, dropout = dropout)
            ]))

    def forward(self, x):
        for attn, ff in self.layers:
            x = attn(x) + x
            x = ff(x) + x

        return self.norm(x)

def create_checkerboard_masks(height, width, device):
    y, x = th.meshgrid(th.arange(height, device=device), th.arange(width, device=device), indexing="ij")

    mask1 = ((x + y) % 2).float()
    mask2 = 1 - mask1

    return mask1, mask2

def alt_pattern_masking(x, t):
    b, c, h, w = x.size()

    mask1, mask2 = create_checkerboard_masks(h, w, device=x.device)

    mask1 = mask1.view(1, 1, h, w).expand(b, -1, -1, -1)
    mask2 = mask2.view(1, 1, h, w).expand(b, -1, -1, -1)

    t = t.view(b, 1, 1, 1)
    t = t / 1000

    mask1 = mask1 * (1 - t) + t
    mask2 = mask2 * (1 - t) + t 

    masked_image1 = x * mask1 
    masked_image2 = x * mask2

    return masked_image1, masked_image2, mask1, mask2



class Diff3M_UNetModel(nn.Module):
    """
    The full UNet model with attention and timestep embedding.

    :param in_channels: channels in the input Tensor.
    :param model_channels: base channel count for the model.
    :param out_channels: channels in the output Tensor.
    :param num_res_blocks: number of residual blocks per downsample.
    :param attention_resolutions: a collection of downsample rates at which
        attention will take place. May be a set, list, or tuple.
        For example, if this contains 4, then at 4x downsampling, attention
        will be used.
    :param dropout: the dropout probability.
    :param channel_mult: channel multiplier for each level of the UNet.
    :param conv_resample: if True, use learned convolutions for upsampling and
        downsampling.
    :param dims: determines if the signal is 1D, 2D, or 3D.
    :param num_classes: if specified (as an int), then this model will be
        class-conditional with `num_classes` classes.
    :param use_checkpoint: use gradient checkpointing to reduce memory usage.
    :param num_heads: the number of attention heads in each attention layer.
    :param num_heads_channels: if specified, ignore num_heads and instead use
                               a fixed channel width per attention head.
    :param num_heads_upsample: works with num_heads to set a different number
                               of heads for upsampling. Deprecated.
    :param use_scale_shift_norm: use a FiLM-like conditioning mechanism.
    :param resblock_updown: use residual blocks for up/downsampling.
    :param use_new_attention_order: use a different attention pattern for potentially
                                    increased efficiency.
    """

    def __init__(
        self,
        image_size,
        in_channels,
        model_channels,
        out_channels,
        num_res_blocks,
        attention_resolutions,
        dropout=0,
        channel_mult=(1, 2, 4, 8),
        conv_resample=True,
        dims=2,
        # num_classes=None,
        one_class=False,
        demo_cat_num=4,
        ehr_cat_num=0,
        use_checkpoint=False,
        use_fp16=False,
        num_heads=1,
        num_head_channels=-1,
        num_heads_upsample=-1,
        use_scale_shift_norm=False,
        resblock_updown=False,
        use_new_attention_order=False,
        # reverse = False,
    ):
        super().__init__()

        if num_heads_upsample == -1:
            num_heads_upsample = num_heads

        self.image_size = image_size
        self.in_channels = in_channels
        self.model_channels = model_channels
        self.out_channels = out_channels
        self.num_res_blocks = num_res_blocks
        self.attention_resolutions = attention_resolutions
        self.dropout = dropout
        self.channel_mult = channel_mult
        self.conv_resample = conv_resample
        # self.num_classes = num_classes
        self.one_class = one_class
        self.use_checkpoint = use_checkpoint
        self.dtype = th.float16 if use_fp16 else th.float32
        self.num_heads = num_heads
        self.num_head_channels = num_head_channels
        self.num_heads_upsample = num_heads_upsample

        time_embed_dim = model_channels * 4
        self.time_embed = nn.Sequential(
            linear(model_channels, time_embed_dim),
            nn.SiLU(),
            linear(time_embed_dim, time_embed_dim),
        )

        self.demo_tokenizer = Demo_Tokenizer(demo_dim=3, out_dim=model_channels * 4, demo_cat_num=demo_cat_num)
        self.ehr_tokenizer = Ehr_Tokenizer(ehr_dim=5, out_dim=model_channels * 4, ehr_cat_num=ehr_cat_num)


        self.input_blocks = nn.ModuleList(
            [
                TimestepEmbedSequential(
                    conv_nd(dims, in_channels, model_channels, 3, padding=1)
                )
            ]
        )

        self.x_start_blocks = nn.ModuleList(
            [
                TimestepEmbedSequential(
                    conv_nd(dims, in_channels, model_channels, 3, padding=1)
                )
            ]
        )

        self._feature_size = model_channels
        input_block_chans = [model_channels]
        ch = model_channels
        ds = 1
        for level, mult in enumerate(channel_mult):
            for _ in range(num_res_blocks):
                layers = [
                    ResBlock(
                        ch,
                        time_embed_dim,
                        dropout,
                        out_channels=mult * model_channels,
                        dims=dims,
                        use_checkpoint=use_checkpoint,
                        use_scale_shift_norm=use_scale_shift_norm,
                    )
                ]
                ch = mult * model_channels
                if ds in attention_resolutions:
                    layers.append(
                        AttentionBlock(
                            ch,
                            use_checkpoint=use_checkpoint,
                            num_heads=num_heads,
                            num_head_channels=num_head_channels,
                            use_new_attention_order=use_new_attention_order,
                        )
                    )
                self.input_blocks.append(TimestepEmbedSequential(*layers))
                self._feature_size += ch
                input_block_chans.append(ch)
            if level != len(channel_mult) - 1:
                out_ch = ch
                self.input_blocks.append(
                    TimestepEmbedSequential(
                        ResBlock(
                            ch,
                            time_embed_dim,
                            dropout,
                            out_channels=out_ch,
                            dims=dims,
                            use_checkpoint=use_checkpoint,
                            use_scale_shift_norm=use_scale_shift_norm,
                            down=True,
                        )
                        if resblock_updown
                        else Downsample(
                            ch, conv_resample, dims=dims, out_channels=out_ch
                        )
                    )
                )
                ch = out_ch
                input_block_chans.append(ch)
                ds *= 2
                self._feature_size += ch

        x_start_ch = model_channels
        x_start_ds = 1
        for level, mult in enumerate(channel_mult):
            for _ in range(num_res_blocks):
                layers = [
                    ResBlock(
                        x_start_ch,
                        time_embed_dim,
                        dropout,
                        out_channels=mult * model_channels,
                        dims=dims,
                        use_checkpoint=use_checkpoint,
                        use_scale_shift_norm=use_scale_shift_norm,
                    )
                ]
                x_start_ch = mult * model_channels
                if x_start_ds in attention_resolutions:
                    layers.append(
                        AttentionBlock(
                            x_start_ch,
                            use_checkpoint=use_checkpoint,
                            num_heads=num_heads,
                            num_head_channels=num_head_channels,
                            use_new_attention_order=use_new_attention_order,
                        )
                    )
                self.x_start_blocks.append(TimestepEmbedSequential(*layers))
            if level != len(channel_mult) - 1:
                x_start_out_ch = x_start_ch
                self.x_start_blocks.append(
                    TimestepEmbedSequential(
                        ResBlock(
                            x_start_ch,
                            time_embed_dim,
                            dropout,
                            out_channels=x_start_out_ch,
                            dims=dims,
                            use_checkpoint=use_checkpoint,
                            use_scale_shift_norm=use_scale_shift_norm,
                            down=True,
                        )
                        if resblock_updown
                        else Downsample(
                            x_start_ch, conv_resample, dims=dims, out_channels=x_start_out_ch
                        )
                    )
                )
                x_start_ch = x_start_out_ch
                x_start_ds *= 2
        
        self.x_start_output_linear = nn.Sequential(
            nn.Linear(x_start_ch, model_channels * 4),
            nn.LayerNorm(model_channels * 4)
        )
        
        self.emb_norm = nn.LayerNorm(model_channels * 4)

        self.middle_block = TimestepEmbedSequential(
            ResBlock(
                ch,
                time_embed_dim,
                dropout,
                dims=dims,
                use_checkpoint=use_checkpoint,
                use_scale_shift_norm=use_scale_shift_norm,
            ),
            AttentionBlock(
                ch,
                use_checkpoint=use_checkpoint,
                num_heads=num_heads,
                num_head_channels=num_head_channels,
                use_new_attention_order=use_new_attention_order,
            ),
            ResBlock(
                ch,
                time_embed_dim,
                dropout,
                dims=dims,
                use_checkpoint=use_checkpoint,
                use_scale_shift_norm=use_scale_shift_norm,
            ),
        )
        self._feature_size += ch

        self.output_blocks = nn.ModuleList([])
        for level, mult in list(enumerate(channel_mult))[::-1]:
            for i in range(num_res_blocks + 1):
                ich = input_block_chans.pop()
                layers = [
                    ResBlock(
                        ch + ich,
                        time_embed_dim,
                        dropout,
                        out_channels=model_channels * mult,
                        dims=dims,
                        use_checkpoint=use_checkpoint,
                        use_scale_shift_norm=use_scale_shift_norm,
                    )
                ]
                ch = model_channels * mult
                if ds in attention_resolutions:
                    layers.append(
                        AttentionBlock(
                            ch,
                            use_checkpoint=use_checkpoint,
                            num_heads=num_heads_upsample,
                            num_head_channels=num_head_channels,
                            use_new_attention_order=use_new_attention_order,
                        )
                    )
                if level and i == num_res_blocks:
                    out_ch = ch
                    layers.append(
                        ResBlock(
                            ch,
                            time_embed_dim,
                            dropout,
                            out_channels=out_ch,
                            dims=dims,
                            use_checkpoint=use_checkpoint,
                            use_scale_shift_norm=use_scale_shift_norm,
                            up=True,
                        )
                        if resblock_updown
                        else Upsample(ch, conv_resample, dims=dims, out_channels=out_ch)
                    )
                    ds //= 2
                self.output_blocks.append(TimestepEmbedSequential(*layers))
                self._feature_size += ch

        self.out = nn.Sequential(
            normalization(ch),
            nn.SiLU(),
            zero_module(conv_nd(dims, model_channels, out_channels, 3, padding=1)),
        )


        self.apm_mask1_input_blocks = copy.deepcopy(self.input_blocks)
        self.apm_mask2_input_blocks = copy.deepcopy(self.input_blocks)

        self.apm_mask_middle_blocks = copy.deepcopy(self.middle_block)

        self.apm_mask_output_blocks = copy.deepcopy(self.output_blocks)
        self.apm_mask_out = nn.Sequential(
            normalization(ch),
            nn.SiLU(),
            zero_module(conv_nd(dims, model_channels, 1, 3, padding=1)),
        )


    def convert_to_fp16(self):
        """
        Convert the torso of the model to float16.
        """
        self.input_blocks.apply(convert_module_to_f16)
        self.middle_block.apply(convert_module_to_f16)
        self.output_blocks.apply(convert_module_to_f16)

    def convert_to_fp32(self):
        """
        Convert the torso of the model to float32.
        """
        self.input_blocks.apply(convert_module_to_f32)
        self.middle_block.apply(convert_module_to_f32)
        self.output_blocks.apply(convert_module_to_f32)

    def forward(self, x, timesteps, demo, demo_cat, demo_indices, x_start, ehr, ehr_cat, ehr_indices):
        """
        Apply the model to an input batch.

        :param x: an [N x C x ...] Tensor of inputs.
        :param timesteps: a 1-D batch of timesteps.
        :param y: an [N] Tensor of labels, if class-conditional.
        :return: an [N x C x ...] Tensor of outputs.
        """

        hs = []
        emb = self.time_embed(timestep_embedding(timesteps, self.model_channels))

        masked_h1, masked_h2, mask1, mask2 = alt_pattern_masking(x.type(self.dtype), timesteps)

        h = x.type(self.dtype)
        for module in self.input_blocks:
            h = module(h, emb)
            hs.append(h)

        hs1 = []
        for module in self.apm_mask1_input_blocks:
            masked_h1 = module(masked_h1, emb)
            hs1.append(masked_h1)

        hs2 = []
        for module in self.apm_mask2_input_blocks:
            masked_h2 = module(masked_h2, emb)
            hs2.append(masked_h2)

        demo_tokens = self.demo_tokenizer(demo, demo_cat, demo_indices)
        ehr_tokens = self.ehr_tokenizer(ehr, ehr_cat, ehr_indices)

        _, _, d = demo_tokens.size()

        x_start_emb = x_start.type(self.dtype)
        for module in self.x_start_blocks:
            x_start_emb = module(x_start_emb, None)

        x_start_emb = F.adaptive_max_pool2d(x_start_emb, (1, 1))
        x_start_emb = x_start_emb.squeeze(-1).squeeze(-1)

        x_start_emb = self.x_start_output_linear(x_start_emb)

        x_start_emb = x_start_emb.unsqueeze(dim=-1)
        dots = th.matmul(demo_tokens, x_start_emb) * (d ** -0.5)
        dots = dots.squeeze(-1)

        demo_w = F.softmax(dots, dim=1)

        demo_w = demo_w.unsqueeze(-1)
        weighted_demo = demo_tokens * demo_w
        demo_cond = weighted_demo.sum(dim=1)

        ehr_dots = th.matmul(ehr_tokens, x_start_emb) * (d ** -0.5)
        ehr_dots = ehr_dots.squeeze(-1)

        ehr_w = F.softmax(ehr_dots, dim=1)

        ehr_w = ehr_w.unsqueeze(-1)
        weighted_ehr = ehr_tokens * ehr_w
        ehr_cond = weighted_ehr.sum(dim=1)

        emb = emb + demo_cond + ehr_cond
        emb = self.emb_norm(emb)

        h = self.middle_block(h, emb)
        masked_h1 = self.apm_mask_middle_blocks(masked_h1, emb)
        masked_h2 = self.apm_mask_middle_blocks(masked_h2, emb)

        for module in self.output_blocks:
            h = th.cat([h, hs.pop()], dim=1)
            h = module(h, emb)
        h = h.type(x.dtype)

        for module in self.apm_mask_output_blocks:
            masked_h1 = th.cat([masked_h1, hs1.pop()], dim=1)
            masked_h1 = module(masked_h1, emb)

        for module in self.apm_mask_output_blocks:
            masked_h2 = th.cat([masked_h2, hs2.pop()], dim=1)
            masked_h2 = module(masked_h2, emb)
        masked_h1 = masked_h1.type(x.dtype)
        masked_h2 = masked_h2.type(x.dtype)

        masked_h1 = self.apm_mask_out(masked_h1)
        masked_h2 = self.apm_mask_out(masked_h2)

        gen_h1 = masked_h1 * mask2
        gen_h2 = masked_h2 * mask1

        gen = gen_h1 + gen_h2

        h = self.out(h)
        h = h[:, 0, :, :].unsqueeze(1)

        return h, gen


class SuperResModel(Diff3M_UNetModel):
    """
    A UNetModel that performs super-resolution.

    Expects an extra kwarg `low_res` to condition on a low-resolution image.
    """

    def __init__(self, image_size, in_channels, *args, **kwargs):
        super().__init__(image_size, in_channels * 2, *args, **kwargs)

    def forward(self, x, timesteps, low_res=None, **kwargs):
        _, _, new_height, new_width = x.shape
        upsampled = F.interpolate(low_res, (new_height, new_width), mode="bilinear")
        x = th.cat([x, upsampled], dim=1)
        return super().forward(x, timesteps, **kwargs)


